    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {
            Trabajador objTraba = new Trabajador();
            TrabajadorDAO dao = new TrabajadorDAO();
            int posicionSeleccionada = cboTipo.getSelectedIndex();
            Tipo_Trabajador objTipoSeleccionada = listartipo.get(posicionSeleccionada);
            objTraba.setNombre(txtNombre.getText());
            objTraba.setApellidoPaterno(txtApePa.getText());
            objTraba.setApellidoMaterno(txtApeMa.getText());
            objTraba.setNroColegiatura(txtNumero.getText());
            objTraba.setTelefono(txtTelefono.getText());
            objTraba.setUsuario(txtUsuario.getText());
            objTraba.setClave(txtClave.getText());
            objTraba.setObjTipoTrabajador(objTipoSeleccionada);
            if (dao.RegistrarTrabajador(objTraba)) {
                  JOptionPane.showMessageDialog(this, "Se registro correctamente al Trabajador ");
                  ListarTrabajadores();
            }else{
                JOptionPane.showMessageDialog(this, "No se registro correctamente al Trabajador ");
            }
        } catch (Exception e) {
            System.out.println("error --> interfaz --> trabajador --> Guardar");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void tblListaTrabajadoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblListaTrabajadoresMouseClicked
        // TODO add your handling code here:
        int fila = tblListaTrabajadores.getSelectedRow();
        if (fila !=-1) {
            objTrabajador = listarTrabajador.get(fila);
            tipoTrab = objTrabajador.getObjTipoTrabajador();
            
            txtNombre.setText(objTrabajador.getNombre());
            txtApePa.setText(objTrabajador.getApellidoPaterno());
            txtApeMa.setText(objTrabajador.getApellidoMaterno());
            txtNumero.setText(objTrabajador.getNroColegiatura());
            txtTelefono.setText(objTrabajador.getTelefono());
            txtUsuario.setText(objTrabajador.getUsuario());
            txtClave.setText(objTrabajador.getClave());
            cboTipo.setSelectedItem(String.valueOf(objTrabajador.getObjTipoTrabajador().getDescripcion()));
        }
    }//GEN-LAST:event_tblListaTrabajadoresMouseClicked

    private void BtnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnActualizarActionPerformed
        // TODO add your handling code here:
        try {
            TrabajadorDAO dao = new TrabajadorDAO();
            Trabajador obtTrabajador = new Trabajador();
            
            obtTrabajador.setNombre(txtNombre.getText());
            obtTrabajador.setApellidoPaterno(txtApePa.getText());
            obtTrabajador.setApellidoMaterno(txtApeMa.getText());
            obtTrabajador.setNroColegiatura(txtNumero.getText());
            obtTrabajador.setTelefono(txtTelefono.getText());
            obtTrabajador.setUsuario(txtUsuario.getText());
            obtTrabajador.setClave(txtClave.getText());
            obtTrabajador.setObjTipoTrabajador(listartipo.get(cboTipo.getSelectedIndex()));
            obtTrabajador.setId(objTrabajador.getId());
            if (dao.ActualizarTrabajador(obtTrabajador)) {
                JOptionPane.showMessageDialog(this, "Se Actualizo correctamente los datos del Trabajador ");
                ListarTrabajadores();
            }else{
                JOptionPane.showMessageDialog(this, "No lo pudimos actualizar por problemas internos ");
            }
        } catch (Exception e) {
            System.out.println(" ERROR --> Interfaz --> Trabajadro --> Actualizar --> " + e.getMessage());
        }
    }//GEN-LAST:event_BtnActualizarActionPerformed

