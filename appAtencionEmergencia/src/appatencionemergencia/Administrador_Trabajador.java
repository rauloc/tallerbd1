    void CargarTipoTrabajador(){
        try {
            DefaultComboBoxModel model = new DefaultComboBoxModel();
            listartipo =  new TipoTrabajadorDAO().ListaDeTipo();
            for (Tipo_Trabajador tipo : listartipo) {
                model.addElement(tipo.getDescripcion());
            }
            cboTipo.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error de base de datos" + e.getMessage());
            System.out.println("Error: Carga Tipo de Trabajador");
        }
    }
    
    void ListarTrabajadores(){
        try {
            TrabajadorDAO dao = new TrabajadorDAO();
            listarTrabajador = dao.ListarTrabajador();
            DefaultTableModel modelo1 = new DefaultTableModel();
            
            modelo1.addColumn("Nombre");
            modelo1.addColumn("Apellido Paterno");
            modelo1.addColumn("Apellido Materno");
            modelo1.addColumn("Tipo Trabajador");
      
            for (Trabajador trabajador : listarTrabajador) {
                modelo1.addRow(new String[]{
                    trabajador.getNombre()+"",
                    trabajador.getApellidoPaterno()+"",
                    trabajador.getApellidoMaterno()+"",
                    trabajador.getObjTipoTrabajador().getDescripcion()+"",
                });
            }
            tblListaTrabajadores.setModel(modelo1);
        } catch (Exception e) {
            System.out.println("error --> interfaz --> trabajador --> listar");
        }
    }
